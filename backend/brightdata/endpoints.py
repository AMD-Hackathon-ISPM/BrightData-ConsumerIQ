from __future__ import annotations

import os
from dataclasses import dataclass, field
from typing import Any


def _envName(endpoint_key: str) -> str:
    normalized = endpoint_key.upper().replace('.', '_').replace('-', '_')
    return f'BRIGHTDATA_DATASET_{normalized}'


@dataclass(frozen=True)
class BrightDataEndpoint:
    key: str
    label: str
    dataset_key: str | None
    dataset_id: str | None = None
    marketplace: str | None = None
    scrape_type: str | None = None
    discover_by: str | None = None
    required_fields: tuple[str, ...] = ()
    optional_fields: tuple[str, ...] = ()
    default_input: dict[str, Any] = field(default_factory=dict)
    notes: str | None = None
    env_name: str | None = None

    def resolved_dataset_id(self) -> str | None:
        env_name = self.env_name or _envName(self.key)
        return os.getenv(env_name) or self.dataset_id

    def public_dict(self) -> dict[str, Any]:
        env_name = self.env_name or _envName(self.key)
        return {
            'key': self.key,
            'label': self.label,
            'marketplace': self.marketplace,
            'datasetKey': self.dataset_key,
            'datasetId': self.dataset_id,
            'datasetIdEnv': env_name,
            'configured': bool(self.resolved_dataset_id()),
            'type': self.scrape_type,
            'discoverBy': self.discover_by,
            'requiredFields': list(self.required_fields),
            'optionalFields': list(self.optional_fields),
            'defaultInput': self.default_input,
            'notes': self.notes,
        }


