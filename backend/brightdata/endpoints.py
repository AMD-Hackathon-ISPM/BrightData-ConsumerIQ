def get_endpoint(key: str) -> BrightDataEndpoint | None:
    return BRIGHTDATA_ENDPOINTS.get(key)


def get_endpoint_registry() -> dict[str, Any]:
    return {'endpoints': [endpoint.public_dict() for endpoint in BRIGHTDATA_ENDPOINTS.values()]}


def validate_endpoint_input(endpoint: BrightDataEndpoint, input_records: list[dict[str, Any]]) -> list[str]:
    errors: list[str] = []
    for index, record in enumerate(input_records):
        for field_name in endpoint.required_fields:
            value = record.get(field_name)
            if value is None or value == '':
                errors.append(f'input[{index}].{field_name} is required for {endpoint.key}')
    return errors
